#ifndef CG_POINTLIGHT_H
#define CG_POINTLIGHT_H
//==============================================================================
#include "light.h"
//==============================================================================
namespace cg {
//==============================================================================
class PointLight : public Light {
 public:
  //----------------------------------------------------------------------------
  // constructors
  //----------------------------------------------------------------------------
  PointLight(const PointLight&) = default;
  PointLight(PointLight&&)      = default;
  PointLight();
  PointLight(double r, double g, double b);
  PointLight(const vec3& spectral_intensity);
  //----------------------------------------------------------------------------
  // methods
  //----------------------------------------------------------------------------
  vec3 incident_radiance_at(const vec3& pos) const override;
  //----------------------------------------------------------------------------
  vec3 light_direction_to(const vec3& pos) const override;
  //----------------------------------------------------------------------------
  double distance_to(const vec3& pos) const override;
  //----------------------------------------------------------------------------
  make_clonable(Light, PointLight);
};
//==============================================================================
}  // namespace cg
//==============================================================================
#endif
